#include<stdio.h>
int main()
{
    char C;
    scanf("%c",&C);
    if (C=='V')
    {
        printf("Violet");
    }
    else if (C=='I')
    {
        printf("Indigo");
    }
    else if (C=='B')
    {
        printf("Blue");
    }
    else if (C=='G')
    {
        printf("Green");
    }
    else if (C=='Y')
    {
        printf("Yellow");
    }
    else if (C=='O')
    {
        printf("Orange");
    }
    else if (C=='R')
    {
        printf("Red");
    }
    else
    {
        printf("-1");
    }
    
}